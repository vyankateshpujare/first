function mathObjectMethods()
{
    console.log("Math.round(6.7) : "+Math.round(6.7));

    console.log("Math.ceil(6.7) : "+Math.ceil(6.7));

    console.log("Math.floor(6.7) : "+Math.floor(6.7));

    console.log("Math.trunc(6.7) : "+Math.trunc(6.7)); //Returns the integer part of number.

    console.log("Math.sign(-73) : "+Math.sign(-73)); //Return -1 for negative, 1 for positive ,0 for 0.

    console.log("Math.pow(6,3) : "+Math.pow(6,3));

    console.log("Math.sqrt(16) : "+Math.round(16));

    console.log("Math.abs(-45) : "+Math.abs(-45));

    console.log("Math.random() : "+Math.random());

    console.log("Math.min(6,72,34,1,12) : "+Math.min(6,72,34,1,12));

    console.log("Math.max(6,72,34,1,12) : "+Math.max(6,72,34,1,12));

    console.log("Math.sin(90) : "+Math.sin(90*(Math.PI/180)));
}
mathObjectMethods();